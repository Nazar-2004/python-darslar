# For loop using
n = int(input("Sonni kiriting:"))
for i in range(1,11): # (1-10) sonlar ro'yxatini tashkil qildik
    print(n, "x", i, "=",n*i)

# while loop
n = int(input("Sonni kiriting:"))
i = 1 # ko'paytirish jadvali musbat bo'lgani uchun
while i <= 10:
    print(n,"x",i,"=",n*i)
    i += 1